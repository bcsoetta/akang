	<h2>Akang revisi 1.3</h2>
	<ul class="list">
		<li>CARNET
			<ul>
				<li>Ada list khusus carnet pada menu pemeriksa</li>
				<li>Untuk input list carnet dilakukan oleh admin pemeriksa melalui menu <strong>request -&gt; pemeriksaan CARNET</strong></li>
				<li>input list dapat dilakukan satu per satu via menu tersebut atau melalui file excel yang diupload via menu tersebut</li>
			</ul>
		</li>
		<li>Catatan pemeriksaan
			<ul>
				<li>Pemeriksa dapat menambahkan catatan pemeriksaan apabila kedapatan barang tidak sesuai</li>
				<li>Barang yang dicatat tidak sesuai dianggap belum selesai, namun tidak masuk dalam daftar outstanding barang yang diperiksa</li>
				<li>Untuk penyelesaian barang yang sudah diflag overtime atau dicatat tidak sesuai, gunakan menu <strong>Browse -&gt; Berdasarkan No AWB</strong></li>
			</ul>
		</li>
		<li>Penunjukkan role pemeriksa
			<ul>
				<li>Selama ini pemeriksa hanya ditugaskan sebagai pemeriksa CN/PIBK. Untuk penunjukkan sebagai pemeriksa CARNET, silahkan centang box CARNET di menu absen petugas.</li>
				<li>Pemeriksa dapat diberikan beberapa role sekaligus (sebagai pemeriksa CN/PIBK sekaligus CARNET), dan dapat ditugaskan di beberapa tempat sekaligus.</li>
			</ul>
		</li>
	</ul>
